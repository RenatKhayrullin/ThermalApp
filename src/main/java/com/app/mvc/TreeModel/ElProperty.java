package com.app.mvc.TreeModel;


public class ElProperty
{
    private String objectProperty;
    private String dataProperty;
    private String valueOfObjectProperty;
    private String valueOfDataProperty;

    public ElProperty() {}

    public String getObjectProperty() {
        return objectProperty;
    }

    public String getDataProperty() {
        return dataProperty;
    }

    public String getValueOfObjectProperty() {
        return valueOfObjectProperty;
    }

    public String getValueOfDataProperty() {
        return valueOfDataProperty;
    }

    public void setObjectProperty(String objectProperty) {
        this.objectProperty = objectProperty;
    }

    public void setDataProperty(String dataProperty) {
        this.dataProperty = dataProperty;
    }

    public void setValueOfObjectProperty(String valueOfObjectProperty) {
        this.valueOfObjectProperty = valueOfObjectProperty;
    }

    public void setValueOfDataProperty(String valueOfDataProperty) {
        this.valueOfDataProperty = valueOfDataProperty;
    }
}
